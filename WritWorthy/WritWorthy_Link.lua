function WritWorthy.FindLink(name)
    local key = WritWorthy.ToLinkKey(name)
    return WritWorthy.LINK[key]
end

-- Santize item name down to a key in the above table
function WritWorthy.ToLinkKey(name)
    local s  = name:gsub("%^.*","") -- strip ^p and other suffixes
    s = s:gsub("|.*","")            -- strip "||plural" ("Fleshfly Larva||Fleshfly Larvae")
    s = s:lower()                   -- monocase
    return s
end

