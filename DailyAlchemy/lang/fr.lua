        {".*fabrique (.*) avec.*suivant.*%c• (.*)%c• (.*)%c• (.*)%c.*",                         "Fabrique |c88AAFF[%1]|r|cFFFFFF...%2, %3, %4|r"},
        {".*fabrique (.*) avec.*suivant.*:(.*)",                                                "Fabrique |c88AAFF[%1]|r|cFFFFFF...%2|r"},

        -- Dayly
        {".*fabrique%w? un%w? (.*) [:：].*",        "Fabrique |c88AAFF[%1]|r"},
        {".*fabrique%w? un%w? (.*)[:：].*",         "Fabrique |c88AAFF[%1]|r"},
        {".*fabrique%w? (.*):.*",                   "Fabrique |c88AAFF[%1]|r"},
        {".*preparez un%w? (.*) [:：].*",           "Fabrique |c88AAFF[%1]|r"}, 
        {".*preparez un%w? (.*)[:：].*",            "Fabrique |c88AAFF[%1]|r"},

        {".*acquer%w%w de l'eau (.*) [:：].*",      "Acquerez |c88AAFF[eau %1]|r"},
        {".*acquer%w%w de l'(absinthe) [:：].*",    "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w um%w? (.*) [:：].*",         "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w um%w? (.*)[:：].*",          "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w un%w? (.*) [:：].*",         "Acquerez |c88AAFF[%1]|r"}, 
        {".*acquer%w%w un%w? (.*)[:：].*",          "Acquerez |c88AAFF[%1]|r"}, 
        {".*acquer%w%w de la (.*) :.*",             "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w du (.*) :.*",                "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w les (.*) :.*",               "Acquerez |c88AAFF[%1]|r"},
        {".*acquer%w%w le (.*) :.*",                "Acquerez |c88AAFF[%1]|r"},
    }
    for _, value in ipairs(list2) do
        if string.match(journalCondition, value[1]) then
            return string.gsub(journalCondition, value[1], value[2])
        end
    end
    return journalCondition
end

