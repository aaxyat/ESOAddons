        {"é",       "e"},
        {"è",       "e"},
        {"ê",       "e"},
        {"â",       "a"},
        {"à",       "a"},
        {" D’",    " de "},
        {" d’",    " de "},
        --{" l' ",    " la "},
        {" IX([:%s])",   " Ⅸ%1"},
        {" VIII([:%s])", " Ⅷ%1"},
        {" VII([:%s])",  " Ⅶ%1"},
        {" VI([:%s])",   " Ⅵ%1"},
        {" IV([:%s])",   " Ⅳ%1"},
        {" V([:%s])",    " Ⅴ%1"},
        {" III([:%s])",  " Ⅲ%1"},
        {" II([:%s])",   " Ⅱ%1"},
        {" I([:%s])",    " Ⅰ%1"},
    }
    for _, value in ipairs(list) do
        journalCondition = string.gsub(journalCondition, value[1], value[2])
    end
    journalCondition = string.lower(journalCondition)


    local list2 = {
        -- Master Writ
        {".*fabrique%w? un%w? (.*) avec.*suivant.*[:：].*%c•%s?(.*)%c•%s?(.*)%c•%s?(.*)%c•.*",  "Fabrique |c88AAFF[%1]|r|cFFFFFF...%2, %3, %4|r"},
