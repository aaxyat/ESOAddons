function DailyAlchemy:ConvertedItemNames(itemName)
    local list = {
        {"(\-)",     "(\-)"},
        {" IX$",     " Ⅸ"},
        {" VIII$",   " Ⅷ"},
        {" VII$",    " Ⅶ"},
        {" VI$",     " Ⅵ"},
        {" IV$",     " Ⅳ"},
        {" V$",      " Ⅴ"},
        {" III$",    " Ⅲ"},
        {" II$",     " Ⅱ"},
        {" I$",      " Ⅰ"},
        {"panacea ", "Panacea "},   -- Some users have string.lower() disabled?
        {" health",  " Health"},    -- Some users have string.lower() disabled?
        {" stamina", " Stamina"},   -- Some users have string.lower() disabled?
    }

    local convertedItemName = itemName
    for _, value in ipairs(list) do
        convertedItemName = string.gsub(convertedItemName, value[1], value[2])
    end
    return {convertedItemName}
end

function DailyAlchemy:ConvertedJournalCondition(journalCondition)
    local list = {
        {" IX([:%s])",   " Ⅸ%1"},
        {" VIII([:%s])", " Ⅷ%1"},
        {" VII([:%s])",  " Ⅶ%1"},
        {" VI([:%s])",   " Ⅵ%1"},
        {" IV([:%s])",   " Ⅳ%1"},
        {" V([:%s])",    " Ⅴ%1"},
        {" III([:%s])",  " Ⅲ%1"},
        {" II([:%s])",   " Ⅱ%1"},
        {" I([:%s])",    " Ⅰ%1"},
    }
    for _, value in ipairs(list) do
        journalCondition = string.gsub(journalCondition, value[1], value[2])
    end
    --journalCondition = string.lower(journalCondition)


    local list2 = {
        {".*Craft a%w? (.*) with.*Traits:%c• (.*)%c• (.*)%c• (.*)%c.*", "Craft |c88AAFF[%1]|r|cFFFFFF...%2, %3, %4|r"},
        {".*Craft(.*)with.*Traits:%c•(.*)%c•(.*)%c•(.*)%c•.*",          "Craft |c88AAFF[%1]|r|cFFFFFF...%2, %3, %4|r"},
        {".*Craft a%w? (.*) with.*properties: (.*)",                    "Craft |c88AAFF[%1]|r|cFFFFFF...%2|r"}, 
        {".*Craft(.*)with.*properties:(.*)",                            "Craft |c88AAFF[%1]|r|cFFFFFF...%2|r"},
        {".*Craft (.*):.*",                                             "Craft |c88AAFF[%1]|r"}, 

        {"Acquire (.*):.*",                                             "Acquire |c88AAFF[%1]|r"}, 
    }
    for _, value in ipairs(list2) do
        if string.match(journalCondition, value[1]) then
            return string.gsub(journalCondition, value[1], value[2])
        end
    end
    return journalCondition
end

